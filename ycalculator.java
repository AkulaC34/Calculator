    private void additionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_additionButtonActionPerformed

        if (jTextField2.getText().length() > 0) {
            secondcalculation();

        } else {

            num1 = Double.parseDouble(jTextField1.getText().replaceAll(",", ""));

        }
        String value2 = Double.toString(num1);
        char c1 = value2.charAt(value2.length() - 1);
        char c2 = value2.charAt(value2.length() - 2);
        if (c1 == '0' && c2 == '.') {
            int num4 = (int) num1;
            jTextField2.setText(intext(String.valueOf(num4)) + " + ");
            num1 = num4;
        } else {
            jTextField2.setText(intext(value2) + " + ");
        }

        cal = 1;
        jTextField1.setText("0");
        screennumber = "";
        se = 0;
        activeoperators = true;

    }//GEN-LAST:event_additionButtonActionPerformed

