        String store;

        if (v1 > 0) {
            StringBuilder type = new StringBuilder(screennumber);
            type.deleteCharAt(v2);
            store = type.toString();
            jTextField1.setText(intext(store));
        }
        if (v1 <= 1) {
            jTextField1.setText("0");
            screennumber = "";
            se = 0;
        }

